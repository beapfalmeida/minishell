#include "libft.h"
#include <stdio.h>
#include <bsd/string.h>
#include <stdlib.h>

/// @brief Copies up to size characters from 
///the src to dst and NUL-terminates the result.
/// @return Length of src
size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	slen;
	size_t	i;

	slen = ft_strlen(src);
	i = 0;
	if (!src || !dst)
		return (0);
	if (size == 0)
		return (slen);
	while (src[i] && i < (size - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (slen);
}
